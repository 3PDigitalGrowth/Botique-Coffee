export function buildAdminNotificationText(
  ctx: SubmissionContext,
  rows: AdminDataRow[],
  headline: string,
): string {
  return [
    headline,
    "",
    "SUBMISSION SOURCE",
    `Form: ${ctx.formLabel}`,
    `Page: ${ctx.pagePath}`,
    `URL: ${ctx.pageUrl}`,
    `When: ${ctx.submittedAtFormatted} (Melbourne)`,
    "",
    "DETAILS",
    ...rows.map((r) => `${r.label}: ${r.valueText}`),
  ].join("\n")
}

