/**
 * Transactional email HTML + plain text for website enquiries.
 * Table-based layout for broad client support; inline styles only.
 */

const COPPER = "#b8651f"
const INK = "#1c1917"
const MUTED = "#57534e"
const WARM_BG = "#faf8f5"
const PAGE_BG = "#ebe8e4"

export type SubmissionContext = {
  formLabel: string
  pagePath: string
  pageUrl: string
  submittedAtFormatted: string
}

export type AdminDataRow = {
  label: string
  valueText: string
  valueHtml: string
}

export function escapeHtml(value: string): string {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;")
}

export function formatMelbourneTimestamp(d: Date): string {
  return new Intl.DateTimeFormat("en-AU", {
    timeZone: "Australia/Melbourne",
    weekday: "long",
    day: "numeric",
    month: "long",
    year: "numeric",
    hour: "numeric",
    minute: "2-digit",
    hour12: true,
  }).format(d)
}

